package renderer.point;

/**
 * This point of this class is to hold the coordinates of our own point
 * @author nikit
 *
 */
public class MyPoint {
	public double x, y, z;	// These are made public so we can use them in other classes.
	
	public MyPoint(double x, double y, double z) {	// Regular constructor for the MyPoint object
		this.x = x;
		this.y = y;
		this.z = z;
	}
	
}
